function calculateSavings() {
  const grossSales = parseFloat(document.getElementById("grossSales").value);
  const commissionRate = parseFloat(document.getElementById("commission").value) / 100;

  if (isNaN(grossSales) || isNaN(commissionRate)) {
    alert("Please enter valid numbers.");
    return;
  }

  const commissionCost = grossSales * commissionRate;

  let tier = '';
  let monthlyFee = 0;

  if (grossSales >= 3000000 && grossSales <= 6000000) {
    tier = 'Tier 1';
    monthlyFee = 4500;
  } else if (grossSales > 6000000 && grossSales <= 10000000) {
    tier = 'Tier 2';
    monthlyFee = 8750;
  } else if (grossSales > 10000000 && grossSales <= 20000000) {
    tier = 'Tier 3';
    monthlyFee = 12500;
  } else if (grossSales > 20000000 && grossSales <= 30000000) {
    tier = 'Tier 4';
    monthlyFee = 20000;
  } else if (grossSales > 30000000 && grossSales <= 50000000) {
    tier = 'Tier 5';
    monthlyFee = 30000;
  } else {
    document.getElementById("results").innerHTML = "<p class='text-danger'>Outside Katalyst platform scope.</p>";
    document.getElementById("results").style.display = 'block';
    return;
  }

  const annualFee = monthlyFee * 12;
  const savings = commissionCost - annualFee;

  document.getElementById("commissionCost").textContent = `$${commissionCost.toLocaleString()}`;
  document.getElementById("tier").textContent = tier;
  document.getElementById("monthlyFee").textContent = `$${monthlyFee.toLocaleString()} / month`;
  document.getElementById("annualFee").textContent = `$${annualFee.toLocaleString()}`;
  document.getElementById("savings").textContent = `$${savings.toLocaleString()}`;

  document.getElementById("results").style.display = 'block';
}
</script>